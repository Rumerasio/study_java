package com.java.study.ch02;

public class CompareOperatorExample {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		System.out.println(a==b);
		
		System.out.println(a != b);
		
		System.out.println(a < b);
		
		System.out.println(a <= b);
		
		System.out.println(a > b);
		
		System.out.println(a >= b);
	}

}
