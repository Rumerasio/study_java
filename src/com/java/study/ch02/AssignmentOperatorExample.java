package com.java.study.ch02;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		
		// +=    -= 
		
		int result = 0;
		
		result +=5;
		// ã„´ result = result +5;
		
		System.out.println("result = "+result);
		
		result -= 2;
		
		System.out.println("result = "+result);
		
		result *= 7;
		System.out.println("result = "+result);
		
				
	}

}
